function initMap() {
    map = new google.maps.Map(document.getElementById('map'), {
        center: defaultLocate,
        disableDefaultUI: true,
        zoom: 11}
    );

    //On click zoom on Comuna
    for (let i = 0; i < communes.length; i++) {
        let tempMarker = new google.maps.Marker({
           position: communes[i],
           map: map,
           title: 'Click para ampliar'
        });
        tempMarker.addListener('click', function() {
            map.setZoom(15);
            map.setCenter(tempMarker.getPosition());
        });
        markers.push(tempMarker);

        let tempPolygon = new google.maps.Polygon({
            paths: communes[i].coords,
            strokeColor: communes[i].color,
            strokeOpacity: 0.6,
            strokeWeight: 1,
            fillColor: communes[i].color,
            fillOpacity: 0.2
        });

        tempPolygon.setMap(map);
        polygons.push(tempPolygon);
   }


   for (let i = 0; i < sensors.length; i++) {
        let tempMarker = new google.maps.Marker({
            position: new google.maps.LatLng(sensors[i].lat, sensors[i].lng),
            map: map,
            icon: 'http://maps.google.com/mapfiles/ms/icons/green-dot.png',
            title: 'Sensor ' + sensors[i].id,
            visible: false
        });

        let infowindow = new google.maps.InfoWindow({
            content: contentString.format(sensors[i].id, "Habilitado", sensors[i].lat, sensors[i].lng, dateNow)
        });

        tempMarker.addListener('click', function() {
            infowindow.open(map, tempMarker);
        });
        sensorMarkers.push(tempMarker)
   }

   google.maps.event.addListener(map, 'zoom_changed', function() {
       var zoom = map.getZoom();
       for (let i = 0; i < markers.length; i++) {
           markers[i].setVisible(zoom < 14);
       }
       for (let j = 0; j < sensorMarkers.length; j++) {
           sensorMarkers[j].setVisible(zoom >= 14);
       }
   });
}


// Sets the map on all markers in the array.
function setMapOnAllMarkers(map) {
    for (let i = 0; i < markers.length; i++) {
        markers[i].setMap(map);
    }
}

// Sets the map on all polygons in the array.
function setMapOnAllPolygons(map) {
    for (let i = 0; i < markers.length; i++) {
        polygons[i].setMap(map);
    }
}

function toogleMarkers(obj) {
    if(obj.checked) {
        setMapOnAllMarkers(map);
    }
    else {
        setMapOnAllMarkers(null);
    }
}

function tooglePolygons(obj) {
    if(obj.checked) {
        setMapOnAllPolygons(map);
    }
    else {
        setMapOnAllPolygons(null);
    }
}

